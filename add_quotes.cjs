  3: "إن قتلتني فلقد قتل من هو شر منك من هو خير مني",
  4: "مَنْ كانَ باذِلاً فِينا مُهْجَتَهُ، وَمُوَطِّناً عَلى لِقاءِ اللهِ نَفْسَهُ، فَلْيَرْحَلْ مَعَنا",
  5: "اللهم إني أعوذ بك من الكرب والبلاء، ههنا مناخ ركابنا، ومسفك دمائنا",
  6: "أميرنا سد منافذ الماء، وضاقت بالعيال الخيام",
  7: "هذا الليل قد غشيكم فاتخذوه جملاً، فإن القوم إنما يطلبونني",
  8: "عباد الله، اتقوا الله وكونوا من الدنيا على حذر",
  9: "اللهم العنهم لعن عاد وثمود، اللهم أبلغ نبيك السلام عني",
  10: "وهكذا توالى الأصحاب يتفانون في حب الله ونصرة وليه",
